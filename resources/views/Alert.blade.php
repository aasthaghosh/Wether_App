                        'Accept': 'application/json',
                        'X-Requested-With': 'XMLHttpRequest',
                        'X-CSRF-TOKEN': csrfToken
                    },
                    body: JSON.stringify({
                        location: locationInput,
                        field_size: fieldSizeInput,
                        primary_crop: cropName,
                        soil_type: soilName
                    })
                });

                if (response.ok) {
                    btn.style.background = 'linear-gradient(135deg, #2e7d32, #4caf50)';
                    btn.innerHTML = '<i class="fas fa-check"></i> Saved!';
                    setTimeout(() => {
                        btn.style.background = '';
                        btn.innerHTML = originalText;
                        btn.disabled = false;
                    }, 2000);
                } else {
                    if (response.status === 419 || response.status === 401) {
                        alert("Session expired. Please refresh the page or log in again.");
                    } else {
                        const errMessage = await response.text();
                        alert("Failed to update profile. Status: " + response.status + "\n" + errMessage);
                        console.error(errMessage);
                    }
                    btn.innerHTML = originalText;
                    btn.disabled = false;
                }
            } catch (error) {
                console.error('Error:', error);
                alert("Network error updating profile.");
                btn.innerHTML = originalText;
                btn.disabled = false;
            }
        });

        // Run initial setup
        updateWeatherForLocation();
        updateDashboard();
        });
