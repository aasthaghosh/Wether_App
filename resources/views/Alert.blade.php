                const task = todoInput.value.trim();
                if (!task) return;

                addTodoBtn.disabled = true;
                addTodoBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i>';

                try {
                    const response = await fetch('{{ route("alert.todo.store") }}', {
                        method: 'POST',
                        headers: {
                            'Content-Type': 'application/json',
                            'Accept': 'application/json',
                            'X-Requested-With': 'XMLHttpRequest',
                            'X-CSRF-TOKEN': csrfToken
                        },
                        body: JSON.stringify({ task: task })
                    });

                    if (response.ok) {
                        const data = await response.json();
                        if (data.success) {
                            // Clear "No tasks" message if it exists
                            if (todoList.children.length === 1 && todoList.children[0].tagName === 'LI' && !todoList.children[0].classList.contains('todo-item')) {
                                todoList.innerHTML = '';
                            }

                            // Append new task
                            const li = document.createElement('li');
                            li.className = 'todo-item';
                            li.setAttribute('data-id', data.todo.id);
                            li.innerHTML = ` <
                    input type = "checkbox"
                class = "todo-checkbox" >
                <
                span class = "todo-text" > $ {
                    data.todo.task
                } < /span>
                `;
                            todoList.prepend(li);
                            todoInput.value = '';
                        }
                    } else {
                        if (response.status === 419 || response.status === 401) {
                            alert("Session expired. Please refresh the page or log in again.");
                        } else {
                            const errMessage = await response.text();
                            alert("Error adding task: " + response.status + "\n" + errMessage);
                            console.error(errMessage);
                        }
                    }
                } catch (error) {
                    console.error("Error adding todo:", error);
                    alert("Network error adding task.");
                } finally {
                    addTodoBtn.disabled = false;
                    addTodoBtn.innerHTML = '<i class="fas fa-plus"></i>';
                }
            });

